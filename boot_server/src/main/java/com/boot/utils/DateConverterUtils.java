    public static List<Date> convertToDateList(List<String> sources) {
        List<Date> list=new ArrayList<>();
        for (String source : sources) {
            list.add(convertToDate(source));
        }
        return list;
    }

    public static Date convertToDate(String source) {
        if (StringUtils.isEmpty(source)) {
            return null;
        }
        source = source.trim();
        try {
            for (Map.Entry<String, String> map : REGX_MAP.entrySet()) {
                if (source.matches(map.getValue())) {
                    return new SimpleDateFormat(map.getKey()).parse(source);
                }
            }
        } catch (ParseException e) {
            e.printStackTrace();
        }
        return null;
    }
